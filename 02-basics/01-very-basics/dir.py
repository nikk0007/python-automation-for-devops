# Use dir() to list all the methods and attributes available for a string object
# "" represents an empty string
print(dir("yyy"))  # Lists string methods and attributes

print("\n")

# Use dir() to list all the methods and attributes available for a list object
# [] represents an empty list
print(dir([]))  # Lists list methods and attributes

print("\n")

# Use dir() to list all the methods and attributes available for a tuple object
# () represents an empty tuple
print(dir(()))  # Lists tuple methods and attributes

print("\n")

# Use dir() to list all the methods and attributes available for a dictionary object
# {} represents an empty dictionary
print(dir({}))  # Lists dictionary methods and attributes

print("\n")
